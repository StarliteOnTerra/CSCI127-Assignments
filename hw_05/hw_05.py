def filterodd(l):
    l = [3, 4, 5, 6, 7, 8, 9, 10, 11]
    for x in l:
        if x % 2 != 1:
            continue
        print(x)            
print(filterodd(1))

def mapsquare(l):
    l = [1, 2, 3, 4, 5]
    for x in l:
        x = x ** 2
        print(x)
print(mapsquare(1))


            
            
        
    